#float format
print("output float number is {}".format(3890.7678687))
print("output float number is {:f}".format(3890.7677))
print("output float number is: {:06.2f}".format(3890.7678687))
print("output float number is; {:4.4f}".format(3890.7678687))
print("output float number is; {:10.4f}".format(3890.7678687))


#decimal format
print("output decimal number is {:7d}".format(38))
print("output decimal number is {:2d}".format(38))
print("output decimal number is {:5d}".format(38))
#
print("output binary number is {:b}".format(389))
print("output octal number is {:o}".format(389))
print("output hexa number is {:x}".format(389))


